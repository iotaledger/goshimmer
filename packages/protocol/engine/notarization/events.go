package notarization

import (
	"github.com/iotaledger/goshimmer/packages/core/ads"
	"github.com/iotaledger/goshimmer/packages/core/commitment"
	"github.com/iotaledger/goshimmer/packages/protocol/ledger"
	"github.com/iotaledger/goshimmer/packages/protocol/ledger/utxo"
	"github.com/iotaledger/goshimmer/packages/protocol/models"
	"github.com/iotaledger/hive.go/runtime/event"
)

// Events is a container that acts as a dictionary for the events of the notarization manager.
type Events struct {
	SlotCommitted *event.Event1[*SlotCommittedDetails]
	Error         *event.Event1[error]

	event.Group[Events, *Events]
}

// NewEvents contains the constructor of the Events object (it is generated by a generic factory).
var NewEvents = event.CreateGroupConstructor(func() (self *Events) {
	return &Events{
		SlotCommitted: event.New1[*SlotCommittedDetails](),
		Error:         event.New1[error](),
	}
})

// SlotMutationsEvents contains the events that are triggered when a mutation is applied to the slot.
type SlotMutationsEvents struct {
	AcceptedBlockRemoved *event.Event1[models.BlockID]

	event.Group[SlotMutationsEvents, *SlotMutationsEvents]
}

// NewSlotMutationsEvents contains the constructor of the SlotMutationsEvents object (it is generated by a generic factory).
var NewSlotMutationsEvents = event.CreateGroupConstructor(func() (self *SlotMutationsEvents) {
	return &SlotMutationsEvents{
		AcceptedBlockRemoved: event.New1[models.BlockID](),
	}
})

type OutputsProvider func(callback func(*ledger.OutputWithMetadata) error) error

// SlotCommittedDetails struct { contains the details of a committed slot.
type SlotCommittedDetails struct {
	Commitment            *commitment.Commitment
	AcceptedBlocks        *ads.Set[models.BlockID, *models.BlockID]
	AcceptedTransactions  *ads.Set[utxo.TransactionID, *utxo.TransactionID]
	SpentOutputs          OutputsProvider
	CreatedOutputs        OutputsProvider
	ActiveValidatorsCount int
}
